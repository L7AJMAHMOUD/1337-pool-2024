#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	fd;

	i = 0;
	fd = 0;
	if (ac >= 1)
	{
		while (av[fd][i])
		{
			write(1, &av[fd][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}
